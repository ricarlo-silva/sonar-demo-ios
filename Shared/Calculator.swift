import Foundation

struct Calculator {

    func add(num1: Int, num2: Int) -> Int {
        return num1 + num2
    }

    func div(num1: Int, num2: Int) -> Int {
        return num1 / num2
    }

    func plus(num1: Int, num2: Int) -> Int {
        return num1 * num2
    }

    func minus(num1: Int, num2: Int) -> Int {
        return num1 - num2
    }
}
