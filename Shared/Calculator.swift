import Foundation

struct Calculator {

    func add(a: Int, b: Int) -> Int {
        return a + b
    }

    func div(a: Int, b: Int) -> Int {
        return a / b
    }

    func plus(a: Int, b: Int) -> Int {
        return a * b
    }

    func minus(a: Int, b: Int) -> Int {
        return a - b
    }
}
